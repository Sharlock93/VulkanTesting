// Physical Device Type Names
char* sh_get_device_type_name(VkPhysicalDeviceType type) {
	switch(type) {
		case VK_PHYSICAL_DEVICE_TYPE_OTHER: return "Other";
		case VK_PHYSICAL_DEVICE_TYPE_INTEGRATED_GPU: return "iGPU";
		case VK_PHYSICAL_DEVICE_TYPE_DISCRETE_GPU: return "dGPU";
		case VK_PHYSICAL_DEVICE_TYPE_VIRTUAL_GPU: return "vGPU";
		case VK_PHYSICAL_DEVICE_TYPE_CPU: return "CPU";
		default: return "";
	}

}
void sh_print_pdevice_info(VkPhysicalDeviceProperties2 *p2) {
	VkPhysicalDeviceProperties *p = &p2->properties;
	log_debug_morel("%s[%s] - VkVersion: %d.%d.%d - Driver: %d.%d.%d",
		p->deviceName,
		sh_get_device_type_name(p->deviceType),
		VK_VERSION_MAJOR(p->apiVersion),
		VK_VERSION_MINOR(p->apiVersion),
		VK_VERSION_PATCH(p->apiVersion),
		VK_VERSION_MAJOR(p->driverVersion),
		VK_VERSION_MINOR(p->driverVersion),
		VK_VERSION_PATCH(p->driverVersion)
	);
}

