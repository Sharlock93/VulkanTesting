void sh_print_memory_type_bits(VkMemoryPropertyFlags flags) {
	u32 size_bits = sizeof(flags)*8;
	for(u32 i = 0; i < size_bits; i++ ) {
		u64 value = flags & (1ULL << i);
		if( value ) 
			log_debug_morel("%s", sh_get_memory_flag_name(value));
	}
}

